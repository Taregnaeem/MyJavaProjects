import java.util.ArrayList;

public class Countlists {
    public static int Count(ArrayList<String> Countes) {
        return Countes.size();
    }

    public static String RemoveLast(ArrayList<String> Cin) {
        return Cin.remove(Cin.size() - 1);
    }

    public static void main(String[] args) {
        ArrayList<String> Listed = new ArrayList<String>();
        Listed.add("Six");
        Listed.add("Seven");
        Listed.add("Eight");
        Listed.add("Ten");
        ArrayList<String> Lis = new ArrayList<String>();
        Lis.add("One");
        Lis.add("two");
        Lis.add("Three");
        Lis.add("Four");
        System.out.println("The number of items in the list is:");
        System.out.println(Count(Lis));
        System.out.println(Count(Listed));
        // Lis.sort();
        // Listed.sort();
        RemoveLast(Lis);
        RemoveLast(Listed);
        System.out.println((Lis));
        System.out.println((Listed));

    }

}
