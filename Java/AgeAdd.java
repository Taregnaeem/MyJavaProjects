public class AgeAdd {
    public static void main(String[] args) {

        int age = 5;
        System.out.println(age);
        age = age + 1;
        System.out.println(age);
        age++;

        System.out.println(age);
        int length = 100;
        System.out.println(length);
        length = 100 * length;
        System.out.println(length);
        length = length / 2;
        System.out.println(length);
        length = (length * 5) + 23;
        System.out.println(length);
    }
}